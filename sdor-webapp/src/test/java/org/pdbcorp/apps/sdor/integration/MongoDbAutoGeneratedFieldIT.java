package org.pdbcorp.apps.sdor.integration;

import static org.assertj.core.api.Assertions.assertThat;

import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.pdbcorp.apps.sdor.data.model.User;
import org.pdbcorp.apps.sdor.data.repository.UserRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.test.annotation.DirtiesContext;
import org.springframework.test.context.junit.jupiter.SpringExtension;

@ExtendWith(SpringExtension.class)
@SpringBootTest
@DirtiesContext
public class MongoDbAutoGeneratedFieldIT {
	@Autowired
	UserRepository userRepository;

	@Test
	public void contextLoads() throws Exception {}

	@DisplayName("Given user object When save Then create new user")
	@Test
	public void validTest() throws Exception {
		User user = new User();
		user.setEmail("john.doe@pdbcorp.org");
		user.setFirstName("John");
		user.setLastName("Doe");
		userRepository.save(user);

		assertThat(userRepository.findAll().size()).isGreaterThan(0);
	}
}